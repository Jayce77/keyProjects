package com.sg.vendingmachinespringmvc.model;

import java.math.BigDecimal;
import java.util.Objects;
import static org.aspectj.org.eclipse.jdt.internal.compiler.parser.TheOriginalJDTParserClass.name;

/**
 *
 * @author jayce
 */
public class Money {
    private MoneyNames name;
    private BigDecimal value;
    private int quantity;
    
    public Money(){
    }
    
    public Money(MoneyNames name, BigDecimal value, int quantity){
        this.name = name;
        this.value = value;
        this.quantity = quantity;
    }

    public MoneyNames getName() {
        return name;
    }

    public void setName(MoneyNames  name) {
        this.name = name;
    }

    public BigDecimal getValue() {
        return value;
    }

    public void setValue(BigDecimal value) {
        this.value = value;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    @Override
    public int hashCode() {
        int hash = 5;
        hash = 43 * hash + Objects.hashCode(this.name);
        hash = 43 * hash + Objects.hashCode(this.value);
        hash = 43 * hash + this.quantity;
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Money other = (Money) obj;
        if (this.quantity != other.quantity) {
            return false;
        }
        if (!Objects.equals(this.name, other.name)) {
            return false;
        }
        if (!Objects.equals(this.value, other.value)) {
            return false;
        }
        return true;
    }
    
    
}
